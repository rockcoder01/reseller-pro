// Default language
let currentLanguage = 'en';

// Function to change the language
function changeLanguage(language) {
    if (!translations[language]) {
        console.error('Language not supported:', language);
        return;
    }
    
    currentLanguage = language;
    localStorage.setItem('preferredLanguage', language);
    
    // Translate all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[language][key]) {
            element.textContent = translations[language][key];
        }
    });
    
    // Translate placeholders with data-i18n-placeholder attribute
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (translations[language][key]) {
            element.placeholder = translations[language][key];
        }
    });
    
    // Update page title if needed
    document.querySelectorAll('[data-i18n-title]').forEach(element => {
        const key = element.getAttribute('data-i18n-title');
        if (translations[language][key]) {
            document.title = translations[language][key] + ' - ReSellPro';
        }
    });
    
    // Dispatch a custom event to notify components that language has changed
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: { language } }));
}

// Function to get a translation for a specific key
function getTranslation(key) {
    if (translations[currentLanguage] && translations[currentLanguage][key]) {
        return translations[currentLanguage][key];
    }
    
    // Fallback to English
    if (translations['en'] && translations['en'][key]) {
        return translations['en'][key];
    }
    
    // If still not found, return the key
    return key;
}

// Initialize language based on stored preference or browser settings
function initializeLanguage() {
    // Check for stored preference
    const storedLanguage = localStorage.getItem('preferredLanguage');
    
    if (storedLanguage && translations[storedLanguage]) {
        currentLanguage = storedLanguage;
    } else {
        // Try to detect language from browser
        const browserLanguage = navigator.language.split('-')[0];
        
        if (translations[browserLanguage]) {
            currentLanguage = browserLanguage;
        } else {
            // Default to English
            currentLanguage = 'en';
        }
    }
    
    // Apply initial translation
    changeLanguage(currentLanguage);
}

// Export functions for use in other scripts
window.i18n = {
    changeLanguage,
    getTranslation,
    getCurrentLanguage: () => currentLanguage
};

// Initialize when DOM is ready
document.addEventListener('DOMContentLoaded', initializeLanguage);